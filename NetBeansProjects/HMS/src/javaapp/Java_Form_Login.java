    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
         Connection connection;
        PreparedStatement ps;
        try {
             
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/health_management_system?zeroDateTimeBehavior=convertToNull", "root", "root");
            ps = connection.prepareStatement("SELECT `userName`, `password` FROM `employee` WHERE `userName` = ? AND `password` = ?");
            
            //uS represents the user that will be inputing the data 
            //The data the user will enter will be send to the Login 
            //Class which will later on be used by the Login Form
            
            Login uS = new Login(jUser.getText(),String.valueOf(jPass.getPassword()));
            
            ps.setString(1, uS.getUserN());
            ps.setString(2, uS.getPassW());
            ResultSet result = ps.executeQuery();
            if(result.next()){
                jLabel_Message.setText("Login Was Successesfully");
                jLabel_Message.setForeground(Color.GREEN);
                timer1.start();
            }
            else{
                jLabel_Message.setText("Invalid Username Or Password");
                jLabel_Message.setForeground(Color.RED);
                timer1.start();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Java_Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton_LoginActionPerformed

